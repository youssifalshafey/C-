#include <iostream>
using namespace std;

int main()
{
    short num1, num2;
    cout << "Enter The Firts number : ";
    cin >> num1;
    cout << "Enter The second number : ";
    cin >> num2;
    cout << num1 << " + " << num2 <<" = "<<num1 + num2 << endl;
    cout << num1 << " - " << num2 <<" = "<<num1 - num2 << endl;
    cout << num1 << " * " << num2 <<" = "<<num1 * num2 << endl;
    cout << num1 << " % " << num2 <<" = "<<num1 % num2 << endl;
    return 0;
}